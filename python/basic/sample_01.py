# Operator
a = 2;
b = 3;

c = a + b;
print(c, type(c))
c = a - b
print(c, type(c))
c = a * b
print(c, type(c))
c = a / b
print(c, type(c))
print('operator //  ----------------')
a = 5
b = 2
c = a // b
print(c, type(c)) # 2

print('operator **  ----------------')
a = 5
b = 2
c = a ** b # a^b
print(c, type(c)) # 9 <class 'int'>

